    public void calcTotalFactura(){
        int randuri = jTable1.getRowCount();
        double total=0;
        
        for (int i = 0; i < randuri; i++) {
            total += Double.valueOf(jTable1.getValueAt(i, 5).toString());
        }
        total_factura.setText(String.valueOf(total));
    }
    
    public void calRest(){
        double sumaPlatita = Double.valueOf(suma_platita.getText());
        double totalFactura = Double.valueOf(total_factura.getText());
        double diferenta = totalFactura - sumaPlatita;
        restDePlata.setText(String.valueOf(diferenta));
    }

