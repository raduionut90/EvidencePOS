    private void adauga_cosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adauga_cosActionPerformed
        //verific daca a fost selectat produs
        if (prod_combo.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Alege un produs");
        } else {

        //adaugare produse in cos
        
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        Vector v = new Vector();
        
        v.add(nr_factura.getText().toString());
        v.add(prod_combo.getSelectedItem().toString());
        v.add(codProdus); //cod de bare
        v.add(prod_cant.getText());
        v.add(u_pret.getText());
        v.add(t_pret.getText());
        
        dt.addRow(v);
        calcTotalFactura();
        calRest();
        }
    }//GEN-LAST:event_adauga_cosActionPerformed

