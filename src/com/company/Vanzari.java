    private void platesteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_platesteActionPerformed
        //trimite factura in bd
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        int randuri = jTable1.getRowCount();
        
        
        try {
        Statement s = db.mycon().createStatement();
        
            for (int i = 0; i < randuri; i++) {
                String nrFact = jTable1.getValueAt(i, 0).toString();
                String numeP = jTable1.getValueAt(i, 1).toString();
                String codP = jTable1.getValueAt(i, 2).toString();
                String cant = jTable1.getValueAt(i, 3).toString();
                String pretU = jTable1.getValueAt(i, 4).toString();
                String pretTotal = jTable1.getValueAt(i, 5).toString();
                cant_total += Integer.parseInt(cant);
                //update vanzari
                s.executeUpdate("INSERT INTO vanzari (nr_fact,nume_p,cod_p,cant,pret_u,pret_t) VALUES ('"+nrFact+"','"+numeP+"','"+codP+"','"+cant+"','"+pretU+"','"+pretTotal+"') ");
            
            }
        JOptionPane.showMessageDialog(null, "Datele au fost salvate");

        } catch (SQLException ex) {
                System.out.println(ex);
            }
        
        try {
            //update facturi `fact_nr`, `cid`, `nume_client`, `total_cant`, `total_valoare`, `status`, `rest`
            int nrFact = Integer.parseInt(nr_factura.getText());
            String numeClient = client_combo.getSelectedItem().toString();
            String totalValoare = total_factura.getText().toString();
            String rest = restDePlata.getText().toString();
            int cl_id = Integer.parseInt(cid);
            String cantTotalafromInt = String.valueOf(cant_total);
            //verific daca fac este platita
           double total =Double.valueOf(totalValoare);
           double restd = Double.valueOf(rest);
           String status = null;
            if (total==restd) {
                status = "neachitat";
            } else if (restd==0){
                status = "achitat";
            } else {
                status = "partial";
            }
            Statement s = db.mycon().createStatement();
            s.executeUpdate("INSERT INTO facturi (fact_nr, cid, nume_client, total_cant, total_valoare, status, rest) VALUES ('"+nrFact+"','"+cl_id+"','"+numeClient+"','"+cantTotalafromInt+"','"+totalValoare+"','"+status+"','"+rest+"') ");

                
        } catch (NumberFormatException | SQLException e) {
            System.out.println(e);
        }
        

    }//GEN-LAST:event_platesteActionPerformed

