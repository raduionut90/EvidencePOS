    private void prod_comboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prod_comboActionPerformed
        
        String nume = prod_combo.getSelectedItem().toString();
        try {
            Statement s = db.mycon().createStatement();
            ResultSet rs = s.executeQuery("SELECT p_pret,p_cod FROM produse WHERE p_nume='"+nume+"' ");
            
            if(rs.next()){
                u_pret.setText(rs.getString("p_pret"));
                codProdus =rs.getString("p_cod");
                cod_produs.setText(codProdus);
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
        cal_total_pret();
        calRest();

        

    }//GEN-LAST:event_prod_comboActionPerformed

